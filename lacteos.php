<h3>La Blanqueada</h3> 

<p>
La Blanqueada Lácteos fue fundada en Abril de 1950 dedicándose inicialmente a la elaboración de 3 especialidades de quesos, 
Sardo, Pategrás y Cuartirolo, alternando con la elaboración periódica de Dulce de Leche.
</p>

<p>
Con el correr del tiempo se sumaron otros tipos de queso a la rutina de elaboración, contando hoy con las siguientes 
variedades de queso: Cremoso, Tybo, Pategrás, Fontina, Sardo, Reggiano, Provolone, Parmesano y Gouda. 60 años de trayectoria 
especializada en la elaboración de productos lácteos artesanales, con materia prima propia hacen que La Blanqueada Lácteos 
este ubicada entre una de las mejores empresas de producción Láctea Artesanal del País.
</p>

<p>
La Empresa posee un predio de 650 hectáreas donde cuenta con un tambo, con más de 500 animales (promedio) Holando Argentino en 
ordeño, cumpliendo con un estricto control sanitario, excelente alimentación y manejo, por parte de nuestro personal calificado. 
La usina láctea está ubicada en el mismo predio, a una distancia de 8 kilómetros de la Ciudad de 9 de Julio, Provincia de Buenos 
Aires equidistante 262 km. de Capital Federal, por la ruta nacional número 5 y a 4 km de la ruta provincial 65, a 100 km de la 
ciudad de Junín hacia el noroeste.    
</p>



