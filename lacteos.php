<h3>Lacteos La Aurora</h3> 

<p>
Lácteos Aurora S.R.L. inició sus actividades a mediados del año 2002 en la ciudad de 9 de Julio, Provincia de Buenos Aires, 
en ese momento el personal de la firma estaba compuesto por tan sólo dos empleados y los dos propietarios. En ese entonces, 
se comercializaba únicamente leche entera en sachet y estaba destinada principalmente al mercado local de la ciudad.
</p>

<p>
Al poco tiempo y con el ímpetu de generar nuevos productos, se suman al portafolio: masa para elaboración de mozzarella y quesos.    
</p>

<p>
Debido a la calidad de la materia prima, a los insumos utilizados y a la constante mejora de los procesos, se expande la zona de 
comercialización lo que genera un aumento en la demanda de nuestros productos.    
</p>

<p>
En noviembre de 2009, se da comienzo a la producción en la nueva planta de elaboración, situada en un predio cercano a la zona 
