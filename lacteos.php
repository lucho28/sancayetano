        <h1>Introduccion a la Industria Lactea</h1>
    </div>
    

    <p>
    La industria láctea es el sector industrial que tiene como materia prima la leche procedente de los animales (por regla general vacas); 
    dicho sector se dedica al procesamiento de este producto para colocarlo en el mercado con todas las características y requisitos de 
    poscódigos alimentarios modernos.
    La industrialización de la leche se refiere al proceso mediante el cual la leche cruda obtenida de vacas u otros animales se somete a 
    una serie de procedimientos para convertirla en productos lácteos procesados y seguros para el consumo humano.
    </p>

    
    <div id="titulos">
        <h2>La industrializacion de lacteos a nivel nacional:</h2>
    </div>

    <p>
    La producción de leche en Argentina se estima en alrededor de 30 millones de litros diarios. Más del 60% de dicho volumen es procesado 
    por 15 grandes empresas multiplanta y  multiproducto, con gran capacidad de procesamiento y tecnología de punta a nivel mundial.
    </p>

    <div id="img">
        <img src="img/lacteos/litros.png" alt="">
    </div>

    <p>
    La industrialización de lacteos depende de la secretaría de agricultura, ganaderia y pesca. Dentro de los atajos “Ganaderia” y 
    “Lecheria podemos ver toda la informacion estadistica de la produccion.” QUe seria la direccion nacional de lechería.
    </p>
   
    <div id="titulos">
        <h2>Los principales pasos a los que es sometida la leche cruda al llegar a la planta procesadora:</h2>
    </div>
    
    
    <p>
    1. Clarificación y separación: Se llevan a cabo por centrifugación, un método que separa sólidos de líquidos de diferente 
    densidad mediante una centrífuga, la cual imprime a la mezcla un movimiento rotatorio con una fuerza mayor que la de la gravedad 
    que provoca la sedimentación de los sólidos o de las partículas de mayor densidad. Al hacer girar la leche cruda, la fuerza 
    centrífuga permite la separación rápida de la grasa, obteniéndose una fracción líquida descremada por un lado, y la crema por otro.
    </p>

    <p>
    Clarificación: Es la remoción de las impurezas sólidas de la leche mediante centrifugación. Las partículas más densas que la fase 
    continua de la leche se dirigen hacia el exterior o perímetro de la centrífuga. Entre estas partículas se encuentran células 
    epiteliales, leucocitos, sedimento bacteriano y materia extraña.    
    </p>

    <p>
    Separación: Es la separación de la grasa de la leche a partir de leche mediante el proceso de centrifugación. Bajo la influencia 
    de la fuerza centrífuga, los glóbulos grasos (crema), que son menos densos que el resto de la leche (leche descremada), se dirigen 
    hacia el centro de la centrífuga o eje de rotación a través de los canales de separación de la centrífuga. La leche descremada, 
    en cambio, se mueve hacia la parte más externa del juego de discos.    
    </p>

    <p>
    2. Estandarización: Normalmente, el contenido de grasa de la leche varía entre especies animales o entre una vaca y otra y que 
    puede deberse a la raza, a la dieta y al estado de lactancia de la vaca, entre otros factores. Con el fin de proveer al consumidor 
    de un producto uniforme, la leche debe ser estandarizada, lo cual también es esencial para la elaboración de productos lácteos. 
    Después de la separación, la grasa se adiciona nuevamente a la leche descremada para obtener un producto con el contenido de grasa deseado. 
    Así puede obtenerse leche entera (30 g/l), parcialmente descremada (28 g/l), semidescremada (16 g/l) o leche descremada (0.5 g/l).
    </p>

    <p>
    3. Pasteurización: Una vez que se han removido las impurezas sólidas de la leche y ésta ha sido estandarizada, es necesario eliminar 
    los microorganismos patógenos presentes para asegurar la inocuidad de la leche. Con este fin, la leche se somete al proceso de 
    pasteurización. El término “pasteurización” se refiere al tratamiento térmico al que se somete la leche o cualquier alimento 
    consistente en una relación de temperatura y tiempo que garantice la destrucción de organismos patógenos.    
    </p>

    <p>
    4.Homogeneización: la grasa de la leche forma normalmente glóbulos, cuyo tamaño varía entre 0.20 y 2.0 µ; la variabilidad en el tamaño 
    de estos glóbulos hace que estos floten hacia la superficie de la leche que se encuentra estática, formando entonces una capa de crema o nata.     
    </p>

    <p>
    El método se lleva a cabo sometiendo la leche a presiones elevadas que fuerzan el paso de la leche a través de pequeños orificios que rompen los glóbulos.    
    </p>

    <p>
    El propósito de la homogeneización es reducir el tamaño de los glóbulos de grasa a menos de 1.0 µ, lo cual permite que permanezcan distribuidos 
    de manera uniforme en la leche, evitando de esta manera que la grasa se separe y flote. Es por ello, que al hervir la leche entera que ha sido 
    homogeneizada, “no hace nata”.    
    </p>

    <p>
    5. Adición de vitaminas: se efectúa con el fin de restablecer las concentraciones normales de vitaminas A y D en las leches con menor contenido 
    de grasa, o bien para que la leche aporte una mayor cantidad de éstas.    
    </p>

    <p>
    6.Envasado: El sistema de envasado depende del proceso de pasteurización al que fue sometida la leche. La leche ultrapasteurizada requiere un 
    envase aséptico o estéril, que es aquél que reúne las condiciones de esterilidad comercial para evitar la presencia de microorganismos en el 
    producto durante el envasado, permitiendo una vida de anaquel mayor.    
    </p>

    <div id="titulos">
       <h2>Cadena de valor</h2> 
    </div>

    <p>
    En Argentina, la cadena láctea se compone casi en su totalidad de leche de origen de bovino y sus productos, siendo marginal la participación 
    de leches de otras especies (oveja, cabra, búfala). El carácter altamente perecedero de la materia prima y de algunos de los productos elaborados; 
    las distancias entre los centros de producción y de consumo; la creciente incorporación de infraestructura de transporte y logística; los procesos 
    productivos diferenciados según líneas de productos; y las heterogeneidades en sus estructuras económicas primarias, industriales y comerciales 
    constituyen características distintivas quecondicionan la dinámica sectorial y la articulación entre agentes participantes en las diferentes etapas.    
    </p>

    <p>
    La cadena láctea se divide, a grandes rasgos, en la producción primaria realizada en los tambos, la etapa industrial y finalmente la distribución 
    y comercialización. Cabe mencionar que los aspectos de transporte y logísticos cobran especial relevancia, tanto en la distribución final como 
    intermedia (desde el tambo a la industria) por el carácter perecedero de los productos, por las distancias recorridas y por su rol en la canasta 
    de consumo dentro del mercado local.
    </p>

    <p>
    A partir de la competencia por la tierra y con los avances tecnológicos, los sistemas predominantes fueron variando. Mientras en la década del 
    ’80 predominaban los sistemas pastoriles que utilizan pasturas y verdeos con poco y nulo suministro de silajes, concentrados y/o subproductos 
    industriales, a fines de los 90’ y en la década de 2000 se generalizaron los sistemas pastoriles con suplementación, mientras que en la 
    actualidad es común observar la presencia de modelos más intensivos, algunos de los cuales involucran el encierre permanente y la alimentación 
    del ganado mediante dietas balanceadas. En los últimos años, se observa una tendencia a una nueva intensificación con dietas en base a diversos 
    concentrados y encierre del ganado. Asimismo, hay un conjunto de tambos que tienen un sistema de estabulado, donde los animales se alimentan a 
    corral y reciben diferentes proporciones de pasturas (cortada y distribuida), silajes, concentrados y/o subproductos industriales.    
    </p>

    <p>
    De acuerdo a INTA (2015), si bien el planteo alimentario sigue siendo principalmente pastoril, la proporción de las pasturas en la distribución 
    de la ocupación de la superficie dedicada al tambo disminuyó del 55% (promedio de 2001-2004) al 47% en 2012/2013, mientras creció la superficie 
    destinada a granos y silaje del 10% al 18%. En este período aumentó la cantidad de tambos que utilizan el silo para alimentación de su ganado 
    del 70% al 100%. Asimismo, se observa un mayor uso de concentrados, la mayor parte de ellos son provistos fuera del establecimiento.    
    </p>

    <p>
    Además de las mejoras en la alimentación, otra característica de la evolución tecnológica en el período es el incremento en la inseminación 
    artificial, que pasó de ser utilizada por el 49% de los tambos, al 70%. A partir de las mejoras productivas y reproductivas se produjo un 
    incremento en la producción diaria promedio de leche del 33%, al tiempo que mejoró el contenido de grasa butirosa -GB- (3,5% a 3,6%) y 
    de proteína bruta - PB- (3,2% a 3,3%)1. En resumen, en los últimos años la lechería ha transitado por un cambio en el uso de los
    factores de producción, como la superficie destinada a la actividad, la carga animal por hectárea, el mayor uso de alimentos concentrados y 
    silaje que permitieron incrementar la productividad media.    
    </p>

    <div id="titulos">
       <h2>Produccion primaria:</h2> 
    </div>

    <p>
    En cuanto a la configuración de la estructura productiva, en la etapa primaria se observa una fuerte heterogeneidad a nivel intrasectorial e 
    interregional, manifestada en una estructura atomizada con cerca de 10.000 tambos, donde coexisten unidades de diferentes tamaños y modelos 
    tecnológicos de producción. Según una encuesta realizada por el INTA (INTA, 2015)2, entre el 2012 y 2013, el 58% de los productores obtiene 
    sus ingresos únicamente de la actividad lechera, mientras que el resto combina actividades con ganadería de carne y/o agricultura. El tamaño 
    promedio del establecimiento tambero fue de 195 has, superficie dedicada en un 70% en promedio a las vacas en ordeño y secas3 (rodeo medio 
    de 175 vacas totales -VT y 143 vacas en ordeño -VO). La producción de leche continuó ligada a las pasturas, pero se incrementó la participación 
    de los cultivos destinados a granos y silaje, participando este último en la alimentación de casi la totalidad del ganado lechero. No obstante 
    estos datos, los tambos registraron mayores compras de granos, aumentando su dependencia de alimentos externos, mostrando una alta diversidad 
    en las estrategias alimenticias. Como respuesta a estas estrategias se produjo un aumento en la carga animal y en la producción individual que 
    repercutió en una mayor producción diaria de leche que pasó entre 2001-2004 y 2012-2013 de 2.150 l/día a 2.870 l/día por tambo, datos que a su 
    vez se tradujeron en una mayor productividad del sistema lechero que subió de una media de 4.980 l año/ha VT a 7.580 l año/ha VT en el mismo 
    período.
    </p>

    <p>
    Sin embargo la fuerte heterogeneidad entre establecimientos persiste, ya que un 44% de los mismos tiene una producción diaria inferior a los 
    2.000 l/día, mientras que el 58% presenta una productividad inferior a la media (década atrás el 54% de los tambos estaba por debajo de la media).    
    </p>

    <p>
    Si se analiza la contribución de los tambos por estrato de tamaño de los tambos, se concluye que el estrato entre 2.000 y 4.000 litros comprende 
    cerca de un tercio de los productores y de la producción láctea nacional. Sin embargo, el 44% de tambos procesan menos de 2.000 litros por día, 
    con un aporte del 18% del total de leche. En este estrato se produce una alta ocupación de mano de obra familiar con una productividad inferior 
    a la del tambo medio y un menor uso de concentrados.    
    </p>

    <div id="img">
        <img src="img/lacteos/distritambo.jpg" alt="">
    </div>

    <p>
    Este proceso de cambios en la estructura de los tambos se acentúa según se comprueba con los datos relevados en la encuesta del INTA 2014/2015. 
    En comparación con los datos relevados en 2012/2013:    
    </p>
    
    <p>
    1. Subió levemente la superficie media de los tambos (206 has), probablemente por una mayor superficie alquilada, debido a que la proporción 
    de establecimientos que tiene una estrategia diversificada con agricultura o ganadería (invernada) se incrementó, llegando al 90%.    
    </p>
    
    <p>
    2. El uso de la superficie ganadera fue similar aunque se registró un nuevo aumento en la utilización de concentrados y silaje, con mayor 
    proporción de maíz por una mejora en la relación precio de la leche: precio del maíz, que subió de 1,9 kg maíz/l leche en 2012/13, a 2,6kg 
    maíz/l leche4. Continuó en aumento la asistencia veterinaria y el control lechero, y se produjo una mejora del status sanitario respecto 
    a la brucelosis y tuberculosis.    
    </p>
    
    <p>
    3. En términos productivos, se registró un aumento de la carga animal y de la producción diaria de leche, que pasó de 2.870 l/día a 2.982 l/día, 
    sin embargo hay una fuerte dispersión en los resultados y persiste un 60% de los tambos que tiene una producción menor a los 3.000 l/día (INTA,2015).    
    </p>

    <div id="titulos">
        <h2>Industria:</h2>
    </div>

    <p>
    En lo que respecta a la etapa industrial, existe una heterogeneidad de empresas, desde aquellas que cuentan con varias plantas, hasta las más 
    pequeñas que son los tambos-fábrica. En este sentido, se utiliza la capacidad diaria de recepción de leche para realizar una estratificación 
    del tipo de empresa láctea.    
    </p>

    <p>
    De acuerdo a información detallada por INTA (INTA, 2010), se pueden observar la participación en la producción de leche, la concentración de 
    las empresas lácteas y de los tambos que aprovisionan de materia prima en los distintos estratos de recepción. En los estratos superiores 
    (más de 50.000 litros) se ubican el 18% de las empresas, entre grandes y medianas grandes, que suelen tener estrategias multiplanta y multiproducto. 
    Estas empresas se aprovisionan de leche con el 78% de los tambos y procesan el 85% de la materia prima.    
    </p>

    <p>
    En los estratos inferiores (menos de 50.000 litros), se encuentran las micro, pequeñas y medianas empresas que representan el 82% del total, 
    se abastecen del 22% de los tambos y elaboran el 15% de la leche cruda.
    Dentro de este grupo hay muchas artesanales, que operan en el circuito informal, característica que aumenta en los períodos de crisis.    
    </p>

    <div id="img">
        <img src="img/lacteos/estratificacion_empresas.jpg" alt="">
    </div>

    <p>
    La estratificación está asociada principalmente a una segmentación por tipo de producto (elaboración de quesos, leche fluida, leche 
    en polvo, dulce de leche, suero en polvo5, etc.).    
    </p>

    <p>
    La industria está fuertemente concentrada. El 5% de las empresas recibe la materia prima del 61% de los tambos y participa del 67% 
    del procesamiento de leche. En el grupo de grandes empresas se encuentran las de origen nacional y las que son filiales de empresas 
    transnacionales. En general se trata de empresas multiplanta y multiproducto que, si bien su principal mercado es el abastecimiento 
    local, tienen una orientación exportadora.    
    </p>

    <p>
    Cabe destacar que las dos principales empresas lácteas procesan un tercio de la leche cruda, con una fuerte orientación hacia el 
    mercado interno para el abastecimiento de leche fluida y productos frescos. Una situación similar se observa en las empresas medianas 
    y medianas grandes, en las que se produjo a fines de los ’90 una serie de fusiones y adquisiciones. En el estrato de empresas medianas, 
    hay una mayor especialización hacia la producción de queso y también de productos de alto valor agregado a menor escala. La orientación 
    general es hacia el mercado interno, aunque cuentan con estrategias comerciales para la exportación. Por último, se encuentra el estrato 
    de empresas pequeñas y tambos-fábrica, que en su mayoría son plantas monoproducto, orientadas a la elaboración de queso, aunque también 
    se encuentran algunas firmas especializadas en otros productos como dulce de leche o que cuentan con algún grado de diversificación. 
    Los tambos-fábrica en general elaboran masa para mozzarella.     
    </p>

    <p>
    En la producción de leche en polvo y deshidratados, por tratarse de commodities con baja generación de valor agregado cobran relevancia 
    las economías de escala. En las empresas procesadoras con esta orientación la escala mínima eficiente es de 200.000 litros de leche 
    procesada por día, con lo cual en general se trata de empresas grandes y medianas/grandes.    
    </p>

    <div id="titulos">
        <h2>Logistica, distribucion y comercializacion</h2>
    </div>
    
    <p>
    En cuanto a la logística de la cadena, es necesario destacar que entre la etapa primaria y la industrial interviene el servicio de recolección 
    y transporte, los actores intervinientes son las flotas de transporte y las plantas de enfriado. El sistema de transporte es a través de 
    camiones cisternas con equipos de frío incorporados. Las modalidades adoptadas varían por región y empresa; mientras que algunas firmas 
    cuentan con flotas propias, otras tercerizan la recolección diaria de leche desde los tambos.    
    </p>

    <p>
    En cuanto a la distribución final, las empresas grandes (de origen nacional) son las que tienen mayor alcance geográfico, ubicando puntos 
    de distribución a lo largo de todo el territorio nacional. Cada una de estas firmas cuenta con una estructura logística propia y abastece 
    alrededor de 80.000 puntos de ventas al consumidor final.    
    </p>

    <p>
    Las ventas de la cadena están dirigidas tanto al mercado interno como externo. Desde la década del ’90 hasta la actualidad, la cantidad de 
    leche comercializada externamente, traducida en cualquier clase de producto lácteo, ha fluctuado alrededor del 20% de la producción primaria, 
    con un pico máximo en 2006 del 28%. Gran parte de las empresas de mayor tamaño participan del comercio internacional con commodities 
    (en especial leche en polvo entera). Para concurrir a este mercado se observan estrategias de colaboración, coordinación de actividades, 
    contratación de servicios de terceros para secar excedentes de leche para la elaboración de leche en polvo y comercialización en forma 
    conjunta ante demandas que excedan sus stocks.    
    </p>

    <p>
    Respecto a la etapa de comercialización, con la consolidación de las grandes cadenas de distribución minorista, en los últimos veinticinco 
    años, la disputa por la participación en la renta hacia el interior de la cadena se ha profundizado. Se estima que cerca del 50% de los puntos 
    de venta de productos lácteos a nivel nacional está concentrado en las grandes cadenas de supermercados quienes son los que establecen las 
    condiciones de la transacción, fijan diferenciales de pago por las posiciones en góndola, plazos y formas de pago, etc. El resto se 
    comercializa en canales minoristas de menor tamaño, con los que las empresas industriales de más envergadura tienen un mayor poder de 
    negociación para fijar condiciones y suelen sugerir el precio de venta al público. Como contraparte, tienen un mayor costo de distribución 
    por la diversidad y dispersión de estos canales de venta.    
    </p>


