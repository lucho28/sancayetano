<h3>Mecano Ganadero</h3>

<p>
Mecano Ganadero S.A. es una compañía argentina especializada en el diseño y la construcción de sistemas de corrales móviles de caños 
con el fin de optimizar el manejo del ganado.
Comenzó en 1968 en 9 de Julio, provincia de Buenos Aires, ofreciendo soluciones a través de instalaciones de madera para el sector ganadero. 
Desde el año 2000 introdujo a la Argentina los sistemas de caño, siendo los primeros en Argentina en reemplazar todos los productos 
de madera por productos de caño.
Hoy en día la empresa ofrece una amplia gama de productos para el cliente ganadero como tranqueras, casillas, paneles y bretes 
brindando soluciones para el campo. Actualmente también comercializa productos para la industria de la construcción.
</p>

