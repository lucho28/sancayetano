        <h2>Descripción de los principales procesos</h2>
    </div>


<h3>Corte:</h3>

<p>
Las operaciones de corte de chapas, perfiles, entre otros, se pueden ejecutar por medios mecánicos o bien utilizando tecnologías de plasma, 
láser, chorro de agua u oxicorte. La selección de la tecnología dependerá del material a cortar, la calidad de corte deseada, los costos, 
entre otros factores.
</p>


<h3>Mecanizado por arranque de viruta:</h3>

<p>
El mecanizado es un proceso de transformación basado en la modificación de la estructura física de una pieza metálica hasta alcanzar 
las especificaciones geométricas definidas. En el mecanizado por arranque de viruta se realiza un desbaste en la pieza metálica modificando 
la geometría mecánicamente, retirando el excedente metálico en forma de viruta con lo que se produce una reducción del peso de partida. 
Los equipos más utilizados para este trabajo, en el sector metalmecánico, son los tornos, los taladros, las fresadoras, entre otras.
</p>


<h3>Soldadura:</h3>

<p>
Se utiliza para realizar el ensamble de las piezas metálicas y dar forma al producto final. Se pueden utilizar distintos tipos de 
soldadura: por arco, a gas, por láser, entre otros.
</p>

<h3>Deformación sin arranque de viruta:</h3>

<p>
La deformación o conformación sin arranque de viruta se basa exclusivamente en la deformación plástica de la pieza mediante la 
aplicación de fuerzas que no modifican el peso de la misma durante el proceso.
</p>

<h3>Desengrase:</h3>

<p>
El desengrase de las superficies metálicas consiste, básicamente, en una operación de limpieza de las mismas. Tiene por objeto 
la eliminación de grasas y aceites que pueda presentar el metal en su superficie procedente de procesos de mecanizado, laminado, 
entre otros.
</p>

