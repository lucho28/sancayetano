       <h2>Cadena de valor</h2> 
    </div>
    

<p>
Las primeras etapas de la producción metalmecánica generalmente se inician con el agregado de valor a insumos provenientes de la industria siderúrgica y de metales no ferrosos, aun cuando a lo largo de toda la cadena también se incorporan insumos y productos terminados de otros rubros. Posteriormente, el proceso de transformación de los metales básicos puede implicar una o múltiples etapas según el grado de complejidad y valor agregado total. Los procesos básicos más comunes son la fundición, la forja, el trefilado, el extruido y el laminado en frío o caliente.

Tras la primera etapa de la cadena de valor metalmecánica resultan los “primeros” productos terminados para consumo final (herramientas, cuchillería, envases, menaje, etc.), construcción e infraestructura (tubos, perfilería de obra, herrería, bronces, etc.) y también para uso industrial, por ejemplo, moldes y matrices. Asimismo, en esta etapa se sientan las bases articuladoras que integran todos los eslabonamientos metalmecánicos “aguas arriba”. El proceso resulta en la fabricación de partes y piezas de diversa complejidad, variedad y contenidos tecnológicos que suelen implicar algún proceso de mecanizado (fresado, torneado, perforado, etc.). De esta etapa posterior resultan las partes y piezas    

</p>
    <p>
    metalmecánicas como las válvulas y cilindros, rodamientos, engranajes, elementos mecánicos, etc.

También corresponde a esta etapa la fabricación de componentes eléctricos y electrónicos. Éstos son provistos directamente a los eslabones más avanzados de la cadena, o bien, previamente, forman parte de algún tipo de ensamblaje electro-metalmecánico (transformadores, sistemas hidráulicos y neumáticos, motores, componentes electrónicos de automatización, módulos mecánicos o eléctricos, etc.). La industria autopartista está enmarcada en este eslabón.

Por último, las etapas finales de la industria metalmecánica están compuestas por los procesos de soldadura, pintura, armado, integración de componentes, ensamblado y embalaje de los rubros de máquinas y equipos (bienes de capital), aparatos de uso doméstico y artefactos eléctricos y electrónicos de diversa complejidad. La oferta de productos que componen estos rubros es heterogénea y diferenciada. También este eslabón de la cadena es el que tiene más relación con el usuario final, brindando servicios de post-venta, realizando la puesta en marcha de los bienes, su mantenimiento y reparación.

Muchas veces las empresas tienden a integrar todo el proceso metalmecánico al interior de sus fábricas, desde la fabricación de partes y piezas hasta su ensamblado final. Es así que conviven empresas altamente especializadas en todos los eslabones, con otras que integran solo aquellas etapas que consideran esenciales para el desarrollo de sus negocios.
Cualquiera sea el caso, a lo largo de toda la producción metalmecánica se observan actividades auxiliares que implican procesos de elevada complejidad, incluyendo tareas claves como la investigación y el desarrollo de productos y procesos, el cumplimiento de normas de calidad, la definición y diseño de modelos en base a requerimientos específicos, entre otros.

